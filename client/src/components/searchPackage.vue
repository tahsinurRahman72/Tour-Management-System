<template>
  <div class="page-header min-vh-50 justify-content-sm-center">
  <div class="direction">
    <input type="text" v-model="input" placeholder="Search your desired destination..." />
    <div class="item dest" v-if="input" v-for="dest in filteredList()" :key="dest">
      <p class="flex-column">{{ dest }}</p>
    </div>
    <div class = "item error align-items-sm-center" v-if="input&&!filteredList().length">
     <p>No results found!</p>
    </div>
  </div>
  </div>
</template>

<script>
export default {
  name: 'search',
  data () {
    return {
      input: '',
      dests: ['Bandarban', 'Rajshahi', 'Sylhet', 'Saint Martin', 'Sundarban']
    }
  },
  methods: {
    filteredList () {
      return this.dests.filter((dest) =>
        dest.toLowerCase().includes(this.input.toLowerCase()))
    }
  }
}
</script>

